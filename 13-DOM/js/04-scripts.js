//QuerySelector
//Distintos tipos de valores como en css

const card = document.querySelector('.card') //Queryselector selecciona el primer valor
console.log(card)

const cards = document.querySelector('.card:nth-child(2)')
console.log(cards)
//Selectores especificos como en CSS
const formulario = document.querySelector('#formulario') //Seleccionando ID
console.log(formulario)

const selecElement = document.querySelector('h1')
